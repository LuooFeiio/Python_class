# 函数的定义
# def 函数名(传入参数):
#     函数体
#     return 返回值
# def 函数名 函数体 别忘记:冒号
# 可以省略 参数 和 return语句
def say_hi():
    print("hi")
    print("这是我写的第一个函数")

# 函数的调用
# 函数名(参数)
say_hi()
# say_hi 不行，要带()

